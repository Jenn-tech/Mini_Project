package sample;

public class Video {
	String vName;
	int vCode;
	int vMoney;
	public Video() {
		
	}
	public String getvName() {
		return vName;
	}
	public void setvName(String vName) {
		this.vName = vName;
	}
	public int getvCode() {
		return vCode;
	}
	public void setvCode(int vCode) {
		this.vCode = vCode;
	}
	public int getvMoney() {
		return vMoney;
	}
	public void setvMoney(int vMoney) {
		this.vMoney = vMoney;
	}
	
	
	
}
